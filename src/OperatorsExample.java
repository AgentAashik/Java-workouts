public class OperatorsExample {
    public static void main(String[] args) {
        int a = 7, b = 12;
        System.out.println("a++ => " + a++);
        System.out.println("--b => " + --b);
        int c = 20;
        System.out.println("++c => " + ++c);
        boolean x = true, y = false;
        System.out.println("x && y => " + (x && y));
        System.out.println("x || y => " + (x || y));
    }
}
